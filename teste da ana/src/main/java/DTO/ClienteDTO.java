package DTO;

public class ClienteDTO {

	private String cnpj;
	private String nomeCliente;
	private String nomeSetor;
	private String objetivoNegocio;
	private String entregaMin;
	private String entregaPossivel;
	



	public String getObjetivoNegocio() {
		return objetivoNegocio;
	}

	public void setObjetivoNegocio(String objetivoNegocio) {
		this.objetivoNegocio = objetivoNegocio;
	}

	public String getEntregaMin() {
		return entregaMin;
	}

	public void setEntregaMin(String entregaMin) {
		this.entregaMin = entregaMin;
	}

	public String getEntregaPossivel() {
		return entregaPossivel;
	}

	public void setEntregaPossivel(String entregaPossivel) {
		this.entregaPossivel = entregaPossivel;
	}

	public String getCnpj() {
		return cnpj;
	}

	public void setCnpj(String cnpj) {
		this.cnpj = cnpj;
	}

	public String getNomeCliente() {
		return nomeCliente;
	}

	public void setNomeCliente(String nomeCliente) {
		this.nomeCliente = nomeCliente;
	}

	public String getNomeSetor() {
		return nomeSetor;
	}

	public void setNomeSetor(String nomeSetor) {
		this.nomeSetor = nomeSetor;
	}








	

}